# Convertir de millas a kilometros 

print("Bienvenidos al conversor de millas a kilometros")

millas = float(input("Cuales son las millas: "))

print(f"Millas ingresadas {millas} millas")

# 1 milla = 1.609 kilometros
kilometros = millas * 1.609

print(f"Los kilometros son {kilometros} kilometros")